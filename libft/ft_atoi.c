#include "libft.h"

int	ft_atoi(const char *str)
{
	int sign;
	int rslt;

	sign = 1;
	rslt = 0;
	while (ft_isspace(*str))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (ft_isdigit(*str))
		rslt = (rslt * 10) + (*str++ - '0');
	rslt *= sign;
	return (rslt);
}
